		HTREEITEM hRootItem = m_testTree.InsertItem("苹果", TVI_ROOT);
		HTREEITEM hSubItem = m_testTree.InsertItem("香蕉", hRootItem);
		m_testTree.InsertItem("查询", hRootItem, hSubItem);
